// Arithmetic Operators in c
#include<stdio.h>
int main(){

    int a, b, c;
    a = 50;
    b = 100;
    c = 150;

    printf("Arithmetic Operators:\n");
    printf("Value of (a + b) = %d\n", (a + b));
    printf("Value of (b - a) = %d\n", (b - a));
    printf("Value of (c / a) = %d\n", (c / a));
    printf("Value of (a * b) = %d\n", (a * b));
    printf("Value of (c % b) = %d\n", (c % b));

    return 0;
}