// Unary Operator in c
#include<stdio.h>
int main(){

    int a = 5;

    printf("Unary Increment Operator\n");
    printf("(a++) = %d\n", a++);
    printf("(++a) = %d\n", ++a);

    printf("Unary Decrement Operator\n");
    printf("(a--) = %d\n", a--);
    printf("(--a) = %d\n", --a);

    return 0;

}