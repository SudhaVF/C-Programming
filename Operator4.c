// Relational / Comparison Operator in c
#include<stdio.h>
int main(){
    int a, b, c;
    a = 5;
    b = 10;
    c = 15;

    printf("Relational Operator\n");
    printf("Value of (a == b) = %d\n", (a == b));
    printf("Value of (a != b) = %d\n", (a != b));
    printf("Value of (b > c) = %d\n", (b > c));
    printf("Value of (c < a) = %d\n", (c < a));
    printf("Value of (c >= b) = %d\n", (c >= b));
    printf("Value of (b <= c) = %d\n", (b <= c));


    return 0;

}